def build_excel(out_path: Path) -> None:
    wb = openpyxl.Workbook()
    wb.remove(wb.active)
    for sheet_name, rows in SHEETS.items():
        ws = wb.create_sheet(sheet_name)
        for row in rows:
            add(ws, row)
    # v1.4: 创建空的 _prev sheet（首次运行；下次构建会被 reader.write_prev_sheet 覆盖）
    if "_prev" not in wb.sheetnames:
        ws = wb.create_sheet("_prev")
        ws.append(["sheet", "分组", "名称", "数值", "单位", "备注", "排序", "is_total"])
    out_path.parent.mkdir(exist_ok=True)
    wb.save(out_path)
    total = sum(len(r) for r in SHEETS.values())
    print(f"Created {out_path} ({out_path.stat().st_size} bytes, {len(SHEETS)} sheets, {total} rows)")


